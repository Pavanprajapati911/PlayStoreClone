package com.example.playstore_clone.reyckerview_models;

public class GameAppContent_Datamodel {

    private String About_this;
    private String Company_name;
    private String Logo;
    private String Name;
    private String Price;
    private String Rated_for;
    private String Ratings;
    private String Size;
    private String Total_downloads;
    private String Total_reviews;
    private String Type;

    public GameAppContent_Datamodel(){

    }

    public String getAbout_this() {
        return About_this;
    }

    public void setAbout_this(String about_this) {
        About_this = about_this;
    }

    public String getCompany_name() {
        return Company_name;
    }

    public void setCompany_name(String company_name) {
        Company_name = company_name;
    }

    public String getLogo() {
        return Logo;
    }

    public void setLogo(String logo) {
        Logo = logo;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getPrice() {
        return Price;
    }

    public void setPrice(String price) {
        Price = price;
    }

    public String getRated_for() {
        return Rated_for;
    }

    public void setRated_for(String rated_for) {
        Rated_for = rated_for;
    }

    public String getRatings() {
        return Ratings;
    }

    public void setRatings(String ratings) {
        Ratings = ratings;
    }

    public String getSize() {
        return Size;
    }

    public void setSize(String size) {
        Size = size;
    }

    public String getTotal_downloads() {
        return Total_downloads;
    }

    public void setTotal_downloads(String total_downloads) {
        Total_downloads = total_downloads;
    }

    public String getTotal_reviews() {
        return Total_reviews;
    }

    public void setTotal_reviews(String total_reviews) {
        Total_reviews = total_reviews;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }
}
